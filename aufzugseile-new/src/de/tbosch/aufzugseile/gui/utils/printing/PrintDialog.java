	/**
	 * Reset the frames for changing language strings.
	 * Reset all check boxes
	 */
	private void resetFrames() {		
		printFrame.dispose();
		printFrameFeyrer.dispose();
    	book = new Book();
    	printFrame = new PrintFrameMain();
		printFrame.insertAufzugschacht(((MainFrame)mainFrame).getAufzugschacht(), (MainFrame)mainFrame);
		printFrame.setVisible(true);
		printFrame.setExtendedState(ICONIFIED);
		book.append(printFrame, pageFormat);
		printFrameFeyrer = new PrintFrameFeyrer();
		printFrameFeyrer.insertUsedFeyrerParameters((MainFrame)mainFrame);
		if (((MainFrame)mainFrame).getBerechnung().getCalcRolls().size() > 0) printFrameFeyrer.createEinzelErgebnisTables((MainFrame)mainFrame);
		printFrameFeyrer.setVisible(true);
		printFrameFeyrer.setExtendedState(ICONIFIED);
		book.append(printFrameFeyrer, pageFormat);
		feyrerRadioButton.setSelected(true);
		parameterRadioButton.setSelected(true);
		ergebnisseRadioButton.setSelected(true);
		aufzugRadioButton.setSelected(true);
		getFooterValues();
	}
