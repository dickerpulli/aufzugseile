    private void feyrerRadioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_feyrerRadioButtonActionPerformed
    	if (feyrerRadioButton.isSelected()) {
    		logger.info("Add Feyrer parameters panel to print frame");
    		Book oldBook = book;
    		book = new Book();
    		if (aufzugRadioButton.isSelected() || parameterRadioButton.isSelected() || ergebnisseRadioButton.isSelected()) {
    			book.append(oldBook.getPrintable(0), pageFormat);
    		}
    		book.append(printFrameFeyrer, pageFormat);
    	}
    	else {
    		logger.info("Remove Feyrer parameters from print frame");
    		Book oldBook = book;
    		book = new Book();
    		if (ergebnisseRadioButton.isSelected() || parameterRadioButton.isSelected() || aufzugRadioButton.isSelected()) {
    			book.append(oldBook.getPrintable(0), pageFormat);
    		}
    	}
    }//GEN-LAST:event_feyrerRadioButtonActionPerformed

    private void ergebnisseRadioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ergebnisseRadioButtonActionPerformed
    	if (ergebnisseRadioButton.isSelected()) {
    		if (!aufzugRadioButton.isSelected() && !parameterRadioButton.isSelected()) {
    			logger.info("Add new first page with solution panel to print frame");
    			Book oldBook = book;
    			book = new Book();
    			book.append(printFrame, pageFormat);
    			if (feyrerRadioButton.isSelected()) {
    				book.append(oldBook.getPrintable(0), pageFormat);
    			}
    		}
    		logger.info("Add solution panel to print frame");
    		if (printFrame.getMainSplitPane().getRightComponent() == null) {
    			printFrame.getMainSplitPane().setRightComponent(printFrame.getUSplitPane());
    			printFrame.getMainSplitPane().setDividerLocation(450);
    			printFrame.getUSplitPane().setDividerLocation(110);
    		}
    		printFrame.getUSplitPane().setRightComponent(printFrame.getUuPanel());
    		printFrame.getUSplitPane().setDividerLocation(110);	
    	}
    	else {
    		if (!parameterRadioButton.isSelected() && !aufzugRadioButton.isSelected()) {
    			logger.info("Remove complete first page print frame");
    			Book oldBook = book;
        		book = new Book();
        		if (feyrerRadioButton.isSelected()) {
        			book.append(oldBook.getPrintable(1), pageFormat);
        		}
    		}
    		else {	
    			logger.info("Remove solution panel from print frame");
    			printFrame.getUSplitPane().setRightComponent(null);
        		if (printFrame.getUSplitPane().getLeftComponent() == null) {
        			printFrame.getMainSplitPane().setRightComponent(null);
        		}
    		}
    	}
    }//GEN-LAST:event_ergebnisseRadioButtonActionPerformed

    private void parameterRadioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_parameterRadioButtonActionPerformed
    	if (parameterRadioButton.isSelected()) {
    		if (!aufzugRadioButton.isSelected() && !ergebnisseRadioButton.isSelected()) {
    			logger.info("Add new first page with parameter panel to print frame");
    			Book oldBook = book;
    			book = new Book();
    			book.append(printFrame, pageFormat);
    			if (feyrerRadioButton.isSelected()) {
    				book.append(oldBook.getPrintable(0), pageFormat);
    			}
    		}
    		logger.info("Add parameter panel to print frame");
    		if (printFrame.getMainSplitPane().getRightComponent() == null) {
    			printFrame.getMainSplitPane().setRightComponent(printFrame.getUSplitPane());
    			printFrame.getMainSplitPane().setDividerLocation(450);
    			printFrame.getUSplitPane().setDividerLocation(110);
    		}
    		if (printFrame.getMainSplitPane().getLeftComponent() == null) {
    			printFrame.getMainSplitPane().setLeftComponent(printFrame.getOSplitPane());
    			printFrame.getMainSplitPane().setDividerLocation(450);
    			printFrame.getUSplitPane().setDividerLocation(110);
    		}
    		printFrame.getOSplitPane().setRightComponent(printFrame.getOrPanel());
    		printFrame.getOSplitPane().setDividerLocation(300);
    		printFrame.getUSplitPane().setLeftComponent(printFrame.getUoPanel());
    		printFrame.getUSplitPane().setDividerLocation(110);
    	}
    	else {
    		if (!ergebnisseRadioButton.isSelected() && !aufzugRadioButton.isSelected()) {
    			logger.info("Remove complete first page print frame");
    			Book oldBook = book;
        		book = new Book();
        		if (feyrerRadioButton.isSelected()) {
        			book.append(oldBook.getPrintable(1), pageFormat);
        		}
    		}
    		else {
    			logger.info("Remove parameter panel from print frame");
    			printFrame.getOSplitPane().setRightComponent(null);
    			printFrame.getUSplitPane().setLeftComponent(null);
    			if (printFrame.getUSplitPane().getRightComponent() == null) {
    				printFrame.getMainSplitPane().setRightComponent(null);
    			}
    			if (printFrame.getOSplitPane().getLeftComponent() == null) {
    				printFrame.getMainSplitPane().setLeftComponent(null);
    			}
    		}
    	}
    }//GEN-LAST:event_parameterRadioButtonActionPerformed

