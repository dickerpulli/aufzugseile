	/**
	 * Gets the footer values.
	 */
	private void getFooterValues() {
		String datei = ((MainFrame)mainFrame).getFilename();
		String nutzer = System.getProperty("user.name");
		Date date = new Date();
		SimpleDateFormat df = new SimpleDateFormat( "dd.MM.yyyy - HH:mm:ss" );
		String datum = df.format(date);

		bezeichnungTextField.setDocument(new TextFieldLimiter(28));
		dateiTextField.setText(datei);
		nutzerTextField.setText(nutzer);
		datumTextField.setText(datum);
		
    	printFrame.getDateiLabel().setText(datei);
    	printFrame.getNutzerLabel().setText(nutzer);
    	printFrame.getDatumLabel().setText(datum);
		
    	printFrameFeyrer.getDateiLabel().setText(datei);
    	printFrameFeyrer.getNutzerLabel().setText(nutzer);
    	printFrameFeyrer.getDatumLabel().setText(datum);
	}
    
